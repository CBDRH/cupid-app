<h1 class="my-3">CUPID HOMEPAGE</h1>

<p>Welcome to the CommUnity Prevention Initiative Dashboard (CUPID). </p>

<p>This free, public resource helps community members, policy makers, and program managers find and understand research on how community action can impact alcohol and other drug (AOD) use, related harms and associated behaviours. CUPID also provides links to recommended resources for AOD prevention.
The evidence behind CUPID comes from global, peer-reviewed evaluation studies of Comprehensive Community Initiatives (CCI). These are approaches that delivered two or more prevention activities from eleven identified initiative subtypes. </p>

<p>In this context: </p>

<ul class="list-disc list-outside pl-5 space-y-2 text-sm text-gray-700">
    <li>
        Community refers to people living in a specific area (like a town or city) or groups of people with shared characteristics (such as First Nations people or students).
    </li>

    <li>
        Comprehensive means initiatives are applied to whole communities and aim to prevent or reduce AOD use and harms at both individual and community levels. 
    </li>

    <li>
        Prevention efforts that focus on a single initiative or only on individuals are not included in the CUPID evidence-base. The included evaluation studies were identified through a systematic review of the literature. 
    </li>

</ul>

