<h2 class="my-3">
    Using the filters
</h2>


When you select filters, CUPID will show a summary of all published evaluation studies of CCI that meet your chosen interests. This summary includes:

<ul class="list-disc list-outside pl-5 space-y-2 text-sm text-gray-700">

    <li>
        How likely the activity is to impact the selected substance of concern use, related harms and associated behaviours
    </li>

    <li>
        A detailed description of each relevant study
    </li>

    <li>
        Links to the associated full-text articles 
    </li>

    <li>
        To help with CCI planning, CUPID also highlights any descriptions of barriers and facilitators for delivering the selected activities.
    </li>

</ul>

<h2 class="mt-6">Data accuracy</h2>

<p>All information in CUPID has been extracted from published evaluation studies by a trained researcher at the National Drug and Alcohol Research Centre and checked by a second researcher. Decisions made around the quality of studies was made independently by two researchers with disagreements resolved through discussion. For more details, visit our Contacts page.</p>

Additional resources
CUPID also provides links to resources for communities interested in delivering CCI and how to evaluate the impact of CCI.

